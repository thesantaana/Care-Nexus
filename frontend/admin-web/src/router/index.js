import { h } from 'vue';

function card(title, lines) {
  return h('article', { class: 'card' }, [
    h('h3', title),
    h('ul', lines.map((line) => h('li', line)))
  ]);
}

function page(title, description, cards) {
  return {
    render() {
      return h('section', { class: 'page' }, [
        h('div', { class: 'page-head' }, [
          h('h2', title),
          h('p', description)
        ]),
        h('div', { class: 'grid' }, cards)
      ]);
    }
  };
}

